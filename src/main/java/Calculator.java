public class Calculator {
    public Calculator(){}

    public int add(int a, int b) {
        return a+b;
    }
    public int sub(int i, int e){
        return i-e;
    }
    public int mult(int a, int b) {
        return a*b;
    }
    public int div2(int c, int d) {
        return c/d;
    }
    public double squareRoot(double a){
        return Math.sqrt(a);
    }

}
